// Reusable FAQItem component
const FAQItem = ({ question, answer, isOpen, onClick }) => (
  <div className="mb-4 border border-gray-200 rounded-lg overflow-hidden">
    <button
      className="w-full text-right bg-white px-6 py-4 flex justify-between items-center focus:outline-none"
      onClick={onClick}
    >
      <span className="text-lg font-semibold text-gray-800">{question}</span>
      <span className="text-gray-500 transform transition-transform duration-300">
        {isOpen ? (
          <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 15l7-7 7 7" />
          </svg>
        ) : (
          <svg className="w-6 h-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
          </svg>
        )}
      </span>
    </button>
    {isOpen && (
      <div className="bg-gray-50 px-6 py-4">
        <p className="text-gray-700">{answer}</p>
      </div>
    )}
  </div>
);

// FAQ Category component
const FAQCategory = ({ title, faqs }) => {
  const [openIndex, setOpenIndex] = useState(null);

  const toggleItem = (index) => {
    setOpenIndex(openIndex === index ? null : index);
  };

  return (
    <div className="mb-10">
      <h2 className="text-2xl font-bold mb-6 text-gray-800">{title}</h2>
      {faqs.map((faq, index) => (
        <FAQItem
          key={index}
          question={faq.question}
          answer={faq.answer}
          isOpen={openIndex === index}
          onClick={() => toggleItem(index)}
        />
      ))}
    </div>
  );
};

