#!/usr/bin/env node
/**
 * Split the monolithic images.ts (468KB / 16,851 lines) into:
 *   1. A slim types + helpers file (~80 lines)
 *   2. Per-category data files (blog.ts, cities.ts, products.ts, etc.)
 *   3. A thin index.ts that re-exports the lookup + helpers
 *
 * Also removes the 3 dead-code redundant exports:
 *   - IMAGES_BY_CATEGORY (~5,000 lines, unused)
 *   - IMAGES_BY_CITY     (~5,000 lines, unused)
 *   - IMAGES_BY_INDUSTRY (~2,000 lines, unused)
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const SRC_FILE = join(ROOT, 'src/lib/data/images.ts');
const OUT_DIR = join(ROOT, 'src/lib/data/images');

// Read the source
const src = readFileSync(SRC_FILE, 'utf8');

// ──────────────────────────────────────────────
// Step 1: Extract the IMAGES object entries
// ──────────────────────────────────────────────
// Find the IMAGES object: starts at "export const IMAGES: Record<string, ImageMetadata> = {"
// and ends at the matching closing brace before "export const IMAGES_BY_CATEGORY"
const imagesStart = src.indexOf('export const IMAGES: Record<string, ImageMetadata> = {');
const imagesByCategory = src.indexOf('export const IMAGES_BY_CATEGORY');
const imagesBlock = src.substring(imagesStart, imagesByCategory).trim();

// Parse: extract the JSON-like content between the first { and last }
const firstBrace = imagesBlock.indexOf('{', imagesBlock.indexOf('='));
const lastBrace = imagesBlock.lastIndexOf('}');
const jsonContent = imagesBlock.substring(firstBrace, lastBrace + 1);

// We'll use a simple approach: eval it in a controlled way
// Actually, let's parse it properly by constructing valid JS
const evalScript = `(${jsonContent})`;
let IMAGES;
try {
  IMAGES = eval(evalScript);
} catch (e) {
  console.error('Failed to parse IMAGES object:', e.message);
  process.exit(1);
}

console.log(`✅ Parsed ${Object.keys(IMAGES).length} image entries`);

// ──────────────────────────────────────────────
// Step 2: Group by category
// ──────────────────────────────────────────────
const byCategory = {};
for (const [slug, meta] of Object.entries(IMAGES)) {
  const cat = meta.category;
  if (!byCategory[cat]) byCategory[cat] = {};
  byCategory[cat][slug] = meta;
}

console.log('Categories found:', Object.keys(byCategory).join(', '));
for (const [cat, entries] of Object.entries(byCategory)) {
  console.log(`  ${cat}: ${Object.keys(entries).length} entries`);
}

// ──────────────────────────────────────────────
// Step 3: Create the output directory
// ──────────────────────────────────────────────
if (!existsSync(OUT_DIR)) {
  mkdirSync(OUT_DIR, { recursive: true });
}

// ──────────────────────────────────────────────
// Step 4: Write types.ts
// ──────────────────────────────────────────────
const typesContent = `/**
 * Image Registry — Types & Helpers
 * Auto-generated by scripts/split-images-registry.mjs
 */

export type ImageCategory =
  | 'heroes'
  | 'cities'
  | 'products'
  | 'fabrics'
  | 'case-studies'
  | 'blog'
  | 'resources'
  | 'trust'
  | 'process'
  | 'testimonials';

export interface ImageVariant {
  width: number;
  height: number;
  path: string;
}

export interface ImageMetadata {
  slug: string;
  category: ImageCategory;
  subCategory: string | null;
  industry: string | null;
  city: string | null;
  path: string;
  webp: string;
  variants: ImageVariant[];
  alt: string;
  description: string;
  width: number;
  height: number;
  gps: { lat: number; lng: number };
  location: {
    sublocation: string;
    city: string;
    state: string;
    country: string;
    countryCode: string;
  };
  dateOriginal: string;
  creatorTool: string;
  keywords: string[];
}

/**
 * Helper: build srcSet string for <picture> or <img>.
 */
export function buildSrcSet(img: ImageMetadata): string {
  return img.variants
    .map(v => \`\${v.path} \${v.width}w\`)
    .concat(\`\${img.path} \${img.width}w\`)
    .join(', ');
}

/**
 * Helper: build a sizes attribute for responsive images.
 */
export function defaultSizes(category: ImageCategory): string {
  switch (category) {
    case 'heroes':
    case 'cities':
    case 'blog':
    case 'resources':
    case 'process':
      return '(max-width: 768px) 100vw, (max-width: 1280px) 75vw, 1920px';
    case 'products':
    case 'fabrics':
      return '(max-width: 768px) 100vw, (max-width: 1280px) 50vw, 1200px';
    case 'case-studies':
      return '(max-width: 768px) 100vw, 50vw';
    case 'trust':
      return '(max-width: 768px) 50vw, 400px';
    case 'testimonials':
      return '(max-width: 768px) 50vw, 200px';
    default:
      return '100vw';
  }
}
`;

writeFileSync(join(OUT_DIR, 'types.ts'), typesContent);
console.log('✅ Written: types.ts');

// ──────────────────────────────────────────────
// Step 5: Write per-category files
// ──────────────────────────────────────────────
const categoryFileNames = {};

for (const [cat, entries] of Object.entries(byCategory)) {
  const safeCat = cat.replace(/-/g, '_');
  const constName = `IMAGES_${safeCat.toUpperCase()}`;
  const fileName = `${cat}.ts`;
  categoryFileNames[cat] = { fileName, constName };

  const content = `/**
 * Image Registry — ${cat} category
 * Auto-generated by scripts/split-images-registry.mjs
 * ${Object.keys(entries).length} entries
 */

import type { ImageMetadata } from './types';

export const ${constName}: Record<string, ImageMetadata> = ${JSON.stringify(entries, null, 2)};
`;

  writeFileSync(join(OUT_DIR, fileName), content);
  console.log(`✅ Written: ${fileName} (${Object.keys(entries).length} entries)`);
}

// ──────────────────────────────────────────────
// Step 6: Write index.ts — thin re-export
// ──────────────────────────────────────────────
const imports = Object.entries(categoryFileNames)
  .map(([cat, { fileName, constName }]) => 
    `import { ${constName} } from './${fileName.replace('.ts', '')}';`
  ).join('\n');

const mergeEntries = Object.values(categoryFileNames)
  .map(({ constName }) => `  ...${constName}`)
  .join(',\n');

const indexContent = `/**
 * Image Registry — Central index
 * Auto-generated by scripts/split-images-registry.mjs
 *
 * Re-exports the unified IMAGES lookup + types + helpers.
 * Each category is in its own file for tree-shaking.
 */

${imports}

export type { ImageCategory, ImageVariant, ImageMetadata } from './types';
export { buildSrcSet, defaultSizes } from './types';

/** Unified image lookup — all categories merged */
export const IMAGES: Record<string, import('./types').ImageMetadata> = {
${mergeEntries}
};
`;

writeFileSync(join(OUT_DIR, 'index.ts'), indexContent);
console.log('✅ Written: index.ts');

// ──────────────────────────────────────────────
// Summary
// ──────────────────────────────────────────────
const totalEntries = Object.keys(IMAGES).length;
const originalSize = (readFileSync(SRC_FILE).length / 1024).toFixed(1);

console.log('');
console.log('═══════════════════════════════════════════');
console.log(`  Original: images.ts = ${originalSize} KB, ${totalEntries} entries`);
console.log(`  Split into ${Object.keys(categoryFileNames).length + 2} files in src/lib/data/images/`);
console.log('  Removed 3 dead-code exports:');
console.log('    - IMAGES_BY_CATEGORY (~5,000 lines)');
console.log('    - IMAGES_BY_CITY     (~5,000 lines)');
console.log('    - IMAGES_BY_INDUSTRY (~2,000 lines)');
console.log('═══════════════════════════════════════════');
console.log('');
console.log('Next steps:');
console.log('  1. Update imports in ResponsiveImage.tsx and imageSchema.ts');
console.log('  2. Delete the old src/lib/data/images.ts');
console.log('  3. Run: npm run build');
