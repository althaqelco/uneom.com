#!/usr/bin/env node
/**
 * Build two consumer-facing artifacts from image-manifest.json:
 *
 *   1. src/lib/data/images.ts — typed registry for use in components
 *      (alt text, srcSet helpers, dimensions, GPS, etc.)
 *
 *   2. public/sitemap-images.xml — static image sitemap announcing all
 *      images to Google Image Search with full ImageObject metadata
 *      (license, geo, caption).
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname } from 'node:path';

const ROOT = '/Users/ahmedsalem/Desktop/all my projects/uneom.comـnew';
const SITE_URL = 'https://uneom.com';
const manifest = JSON.parse(readFileSync(`${ROOT}/scripts/image-manifest.json`, 'utf-8'));
const META = JSON.parse(readFileSync(`${ROOT}/scripts/image-data.json`, 'utf-8'));

// =========== 1. TypeScript registry ===========

const tsHeader = `/**
 * Image Registry — auto-generated by scripts/build-manifests.mjs
 * Source: scripts/image-manifest.json (do NOT edit by hand)
 *
 * For each image we publish:
 *   - path: AVIF main file
 *   - webp: WebP fallback for browsers without AVIF support
 *   - srcSet: responsive AVIF variants for <picture>/Next Image
 *   - alt: accessibility + SEO text
 *   - dimensions, GPS, location, dateOriginal — surfaced for ImageObject schema
 */

export type ImageCategory =
  | 'heroes'
  | 'cities'
  | 'products'
  | 'fabrics'
  | 'case-studies'
  | 'blog'
  | 'resources'
  | 'trust'
  | 'process'
  | 'testimonials';

export interface ImageVariant {
  width: number;
  height: number;
  path: string;
}

export interface ImageMetadata {
  slug: string;
  category: ImageCategory;
  subCategory: string | null;
  industry: string | null;
  city: string | null;
  path: string;            // primary AVIF, e.g. "/images/cities/hero-riyadh.avif"
  webp: string;            // primary WebP fallback
  variants: ImageVariant[];
  alt: string;
  description: string;     // long description from manifest
  width: number;
  height: number;
  gps: { lat: number; lng: number };
  location: {
    sublocation: string;
    city: string;
    state: string;
    country: string;
    countryCode: string;
  };
  dateOriginal: string;    // EXIF DateTimeOriginal
  creatorTool: string;
  keywords: string[];
}

`;

function buildAltText(img) {
  const { slug, category, subCategory, industry, city } = img;
  const cityData = city && META.cities[city];
  const industryData = industry && META.industries[industry];

  switch (category) {
    case 'heroes':
      return `UNEOM ${industryData?.en || 'professional'} uniforms — Saudi B2B leader in ${industryData?.en?.toLowerCase() || 'workwear'} attire.`;
    case 'cities':
      return `UNEOM professional uniforms serving ${cityData?.en} in ${cityData?.region}, Saudi Arabia.`;
    case 'products': {
      const prod = META.products[slug];
      if (prod) return `${prod.en} — UNEOM ${industryData?.en || ''} line, ${prod.fabric}.`;
      return `UNEOM ${industryData?.en || ''} product — ${slug.replace(/-/g, ' ')}.`;
    }
    case 'fabrics':
      return `Fabric macro: ${slug.replace(/-macro$/, '').replace(/-/g, ' ')} — UNEOM textile catalog.`;
    case 'case-studies': {
      const action = slug.includes('after') ? 'after UNEOM transformation' : slug.includes('before') ? 'before UNEOM engagement' : 'UNEOM case study';
      return `${subCategory?.replace(/-/g, ' ')} — ${action}.`;
    }
    case 'blog':
      return `Editorial: ${slug.replace(/-/g, ' ')} — UNEOM industry insights.`;
    case 'resources':
      return `Resource guide: ${slug.replace(/-hero$/, '').replace(/-/g, ' ')} — UNEOM expertise.`;
    case 'trust':
      return `${slug.replace(/-/g, ' ').toUpperCase()} compliance verification badge — UNEOM certified.`;
    case 'process':
      return `UNEOM process diagram: ${slug.replace(/-/g, ' ')}.`;
    case 'testimonials':
      return `UNEOM team member or expert reviewer portrait.`;
    default:
      return `UNEOM image: ${slug.replace(/-/g, ' ')}.`;
  }
}

function toIdentifier(slug) {
  // Convert kebab-case to camelCase for TS keys
  return slug.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}

const enriched = manifest.images.map(img => ({
  ...img,
  path: `/images/${img.path}`,
  webp: img.webp,
  alt: buildAltText(img)
}));

// Group by category for ergonomic access
const grouped = {};
for (const img of enriched) {
  if (!grouped[img.category]) grouped[img.category] = {};
  const subKey = img.subCategory ? `${img.subCategory}/${img.slug}` : img.slug;
  grouped[img.category][subKey] = img;
}

let ts = tsHeader;
ts += `export const IMAGES: Record<string, ImageMetadata> = ${JSON.stringify(
  Object.fromEntries(enriched.map(i => [`${i.category}/${i.subCategory ? i.subCategory + '/' : ''}${i.slug}`, i])),
  null,
  2
)};\n\n`;

ts += `export const IMAGES_BY_CATEGORY: Partial<Record<ImageCategory, ImageMetadata[]>> = ${JSON.stringify(
  Object.fromEntries(
    Object.entries(
      enriched.reduce((acc, img) => {
        if (!acc[img.category]) acc[img.category] = [];
        acc[img.category].push(img);
        return acc;
      }, {})
    )
  ),
  null,
  2
)} as const;\n\n`;

ts += `export const IMAGES_BY_CITY: Record<string, ImageMetadata[]> = ${JSON.stringify(
  enriched.reduce((acc, img) => {
    if (!img.city) return acc;
    if (!acc[img.city]) acc[img.city] = [];
    acc[img.city].push(img);
    return acc;
  }, {}),
  null,
  2
)};\n\n`;

ts += `export const IMAGES_BY_INDUSTRY: Record<string, ImageMetadata[]> = ${JSON.stringify(
  enriched.reduce((acc, img) => {
    if (!img.industry) return acc;
    if (!acc[img.industry]) acc[img.industry] = [];
    acc[img.industry].push(img);
    return acc;
  }, {}),
  null,
  2
)};\n\n`;

ts += `/**
 * Helper: build srcSet string for <picture> or <img>.
 * Returns: "path-640.avif 640w, path-960.avif 960w, ..."
 */
export function buildSrcSet(img: ImageMetadata): string {
  return img.variants
    .map(v => \`\${v.path} \${v.width}w\`)
    .concat(\`\${img.path} \${img.width}w\`)
    .join(', ');
}

/**
 * Helper: build a sizes attribute for responsive images.
 * Sensible defaults per category.
 */
export function defaultSizes(category: ImageCategory): string {
  switch (category) {
    case 'heroes':
    case 'cities':
    case 'blog':
    case 'resources':
    case 'process':
      return '(max-width: 768px) 100vw, (max-width: 1280px) 75vw, 1920px';
    case 'products':
    case 'fabrics':
      return '(max-width: 768px) 100vw, (max-width: 1280px) 50vw, 1200px';
    case 'case-studies':
      return '(max-width: 768px) 100vw, 50vw';
    case 'trust':
      return '(max-width: 768px) 50vw, 400px';
    case 'testimonials':
      return '(max-width: 768px) 50vw, 200px';
    default:
      return '100vw';
  }
}
`;

const tsOut = `${ROOT}/src/lib/data/images.ts`;
mkdirSync(dirname(tsOut), { recursive: true });
writeFileSync(tsOut, ts);
console.log(`✓ TypeScript registry → ${tsOut} (${enriched.length} images)`);

// =========== 2. Image sitemap ===========

const escape = (s) => String(s)
  .replace(/&/g, '&amp;')
  .replace(/</g, '&lt;')
  .replace(/>/g, '&gt;')
  .replace(/"/g, '&quot;')
  .replace(/'/g, '&apos;');

let xml = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
        xmlns:image="http://www.google.com/schemas/sitemap-image/1.1">
`;

// Group images by their associated page URL for proper sitemap structure.
// One <url> per page, with multiple <image:image> entries inside.
function pageUrlFor(img) {
  switch (img.category) {
    case 'heroes':
      return `${SITE_URL}/industries/${img.industry}/`;
    case 'cities':
      return `${SITE_URL}/locations/${img.city}/`;
    case 'products':
      return `${SITE_URL}/shop/${img.subCategory}/${img.slug}/`;
    case 'fabrics':
      return `${SITE_URL}/resources/fabric-guide/`;
    case 'case-studies':
      return `${SITE_URL}/case-studies/${img.subCategory}/`;
    case 'blog':
      return `${SITE_URL}/blog/${img.slug}/`;
    case 'resources':
      return `${SITE_URL}/resources/${img.slug.replace(/-hero$/, '')}/`;
    case 'trust':
      return `${SITE_URL}/about/compliance/`;
    case 'process':
      return `${SITE_URL}/services/${img.slug}/`;
    default:
      return `${SITE_URL}/`;
  }
}

const byPage = enriched.reduce((acc, img) => {
  const url = pageUrlFor(img);
  if (!acc[url]) acc[url] = [];
  acc[url].push(img);
  return acc;
}, {});

for (const [pageUrl, images] of Object.entries(byPage)) {
  xml += `  <url>\n    <loc>${escape(pageUrl)}</loc>\n`;
  for (const img of images) {
    xml += `    <image:image>\n`;
    xml += `      <image:loc>${escape(SITE_URL + img.path)}</image:loc>\n`;
    xml += `      <image:title>${escape(img.alt.slice(0, 100))}</image:title>\n`;
    xml += `      <image:caption>${escape(img.alt)}</image:caption>\n`;
    if (img.location?.city) xml += `      <image:geo_location>${escape(img.location.city + ', ' + img.location.country)}</image:geo_location>\n`;
    xml += `      <image:license>${SITE_URL}/license/</image:license>\n`;
    xml += `    </image:image>\n`;
  }
  xml += `  </url>\n`;
}

xml += '</urlset>\n';

const sitemapOut = `${ROOT}/public/sitemap-images.xml`;
writeFileSync(sitemapOut, xml);
console.log(`✓ Image sitemap   → ${sitemapOut} (${Object.keys(byPage).length} pages, ${enriched.length} images)`);

// =========== 3. Quick stats ===========
console.log('');
console.log('Distribution:');
const byCategory = enriched.reduce((acc, i) => {
  acc[i.category] = (acc[i.category] || 0) + 1;
  return acc;
}, {});
Object.entries(byCategory).sort((a, b) => b[1] - a[1]).forEach(([cat, n]) => {
  console.log(`  ${cat.padEnd(15)} ${n}`);
});
