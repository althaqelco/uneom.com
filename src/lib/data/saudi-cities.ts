// Helper functions
export function getCityBySlug(slug: string): SaudiCity | undefined {
  return SAUDI_CITIES.find((c) => c.slug === slug);
}

export function getAllCitySlugs(): string[] {
  return SAUDI_CITIES.map((c) => c.slug);
}

export function getCitiesByIndustry(industry: string): SaudiCity[] {
  return SAUDI_CITIES.filter((c) => c.dominantIndustries.includes(industry));
}

